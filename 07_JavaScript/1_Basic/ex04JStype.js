// 1. number : 정수, 실수 등 산술 연산이 가능한 자료형
let num1 = 1.234;
let num2 = 50;
// typeof : 자료형을 보여주는 명령어(함수)
console.log(typeof(num1));
console.log(typeof(num2));

// 2. string : 문자열로 이루어진 자료형
let str1 = '김민지';
let str2 = 'smhrd';
// ", ' 둘 다 사용 가능
let str3 = '저는 "김민지"입니다.'
let str4 = '저는 \'김민지\'입니다.'
console.log(typeof(str3));
